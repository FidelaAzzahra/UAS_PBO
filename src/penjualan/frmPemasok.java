    private void cmdSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmdSimpanMouseClicked
        // TODO add your handling code here:
        String tKode=txtKode.getText();
        String tNamaPms=txtNama_Pms.getText();
        String tNama=txtNama.getText();
        String tKota=txtKota.getText();
        String tPos=txtKd_Pos.getText();
        String tTelp=txtTelepon.getText();
        
    try{
        if (edit==true){
            stm.executeUpdate("UPDATE pemasok SET nm_pms='"+tNamaPms+"',nm_brg='"+tNama+"',kota_pms='"+tKota+"',kd_pos='"+tPos+"',phone='"+tTelp+"' WHERE kd_pms='" + tKode + "'");
        }else{
            stm.executeUpdate("INSERT INTO pemasok VALUES('"+tKode+"','"+tNamaPms+"','"+tNama+"','"+tKota+"','"+tPos+"','"+tTelp+"')");
        }
        tblPms.setModel(new DefaultTableModel(dataTable,header));
        baca_data();
        aktif(false);
        setTombol(true);
    }catch(SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_cmdSimpanMouseClicked

