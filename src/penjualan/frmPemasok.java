    //method baca data dari Mysql dimasukkan ke table pada form
    private void baca_data()
    {
        try{
           stm = Con.createStatement(); 
           
           pstat = Con.prepareStatement("select * from pemasok",ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
           RsPms = pstat.executeQuery();

           ResultSetMetaData meta = RsPms.getMetaData();
           int col = meta.getColumnCount();
           int baris = 0;

           while(RsPms.next()) {
               baris = RsPms.getRow();
           }

           dataTable = new Object[baris][col];
           int x = 0;
           RsPms.beforeFirst(); // mengarahkan kursor ke data sebelum data awal

           while(RsPms.next()) {
               dataTable[x][0] = RsPms.getString("kd_pms");
               dataTable[x][1] = RsPms.getString("nm_pms"); 
               dataTable[x][2] = RsPms.getString("nm_brg");
               dataTable[x][3] = RsPms.getString("kota_pms");
               dataTable[x][4] = RsPms.getString("kd_pos");
               dataTable[x][5] = RsPms.getString("phone"); 
            x++;
           }
           
           tblPms.setModel(new DefaultTableModel(dataTable,header));
        }
        catch(SQLException e)
        {
           JOptionPane.showMessageDialog(null, e);
        }
    }   
    
